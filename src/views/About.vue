<script>
import BMap from "BMap"
import { onMounted } from "vue"
export default {
  setup() {
    const setMapEvent = () => {
      window.map.enableDragging() //启用地图拖拽事件，默认启用(可不写)
      window.map.enableScrollWheelZoom() //启用地图滚轮放大缩小
      window.map.enableDoubleClickZoom() //启用鼠标双击放大，默认启用(可不写)
      window.map.enableKeyboard() //启用键盘上下左右键移动地图
    }
    const addMarker = () => {
      const markerArr = [
        {
          title: "惠通(浙江)控股有限公司",
          content:
            "<br/>联系方式：4001185690<br/><br/>企业邮箱：htkgyxgs@163.com<br/><br/>地&nbsp;址：浙江省杭州市滨江区西兴<br/>街道江陵路1916号兴祺大<br/>厦1幢1803室",
          point: "120.222488|30.217639",
          isOpen: 0,
          icon: {
            w: 66,
            h: 73,
            l: 0,
            t: 0,
            x: 6,
            lb: 5,
          },
        },
      ]
      for (let i = 0; i < markerArr.length; i++) {
        // eslint-disable-next-line no-var
        var json = markerArr[i]
        const p0 = json.point.split("|")[0]
        // eslint-disable-next-line no-var
        var p1 = json.point.split("|")[1]
        const point = new BMap.Point(p0, p1)
        // eslint-disable-next-line @typescript-eslint/no-use-before-define
        const iconImg = createIcon(json.icon)
        const marker = new BMap.Marker(point, {
          icon: iconImg,
        })
        const label = new BMap.Label(json.title, {
          offset: new BMap.Size(json.icon.lb - json.icon.x + 10, -20),
        })
        marker.setLabel(label)
        window.map.addOverlay(marker)
        label.setStyle({
          borderColor: "#808080",
          color: "#333",
          cursor: "pointer",
          display: "none",
        })
      }
    }
    const addMapControl = () => {
      // eslint-disable-next-line @typescript-eslint/camelcase
      const ctrl_sca = new BMap.ScaleControl({
        // eslint-disable-next-line no-undef
        anchor: BMAP_ANCHOR_BOTTOM_LEFT,
      })
      window.map.addControl(ctrl_sca)
    }
    //创建一个Icon
    const createIcon = (json) => {
      const icon = new BMap.Icon(
        "https://image.vxiaoke360.com/dizhi.png",
        new BMap.Size(json.w, json.h),
        {
          imageOffset: new BMap.Size(-json.l, -json.t),
          infoWindowOffset: new BMap.Size(json.lb + 5, 1),
          offset: new BMap.Size(json.x, json.h),
        }
      )
      return icon
    }
    onMounted(() => {
      const map = new BMap.Map("dituContent") //在百度地图容器中创建一个地图
      const point = new BMap.Point(120.222488, 30.217639) //定义一个中心点坐标
      map.centerAndZoom(point, 18) //设定地图的中心点和坐标并将地图显示在地图容器中
      window.map = map
      setMapEvent() //设置地图事件
      addMapControl() //向地图添加控件
      addMarker() //向地图中添加marker
    })
    return {
      // setMapEvent,
      // addMarker,
      // addMapControl
    }
  },
}
</script>
